           <p> Picture galleries are very important to our website. Let see how to create an html gallery.
	“Visual design is often essential for the success of a web presence, and is equally important for both blogs and web stores. While it’s vital for website operators to observe the oft-repeated mantra, ‘Content is King’, and place a strong emphasis on creating effective content, the website’s visual aspect should never be neglected. Large blocks of text can often scare visitors off before they even read the content. Graphics and images are therefore hugely important elements of any website, whether they’re used to illustrate a text, to create a personal or emotional bond with the user, or to visualize the details of a product. Galleries are a popular way of integrating images into a website, allowing users to see several images clearly at once. There are several diverse methods of generating slide shows such as these, including embedding a simple Lightbox gallery in your website with a source code and implementing the photo gallery via HTML. Another, even simpler method involves using a popular content management system. To do this with a system like TYPO3 or WordPress, users just need to find the right extension and integrate it into the basic framework.Creating a Lightbox gallery is one of the simplest ways of presenting images on a website. Lightboxes are programmed in Javascript as dynamic HTML. When a user clicks on the preview image, that picture opens up in a larger format as the website fades in the background. This kind of Lightbox can be embedded easily into a website and extended with further functions. To create a gallery, users require a current version of Lightbox, and of course, all the images (including thumbnails), that are to be embedded on the site. The first version of Lightbox was developed by Lokesh Dhakar in 2005 and the script became available under a Creative Commons License a short while later. Since then, the popular script has released several new versions and updates, including both standalone scripts and plugins for JavaScript libraries and extensions for content management systems.”(“Web design basics: creating a photo gallery for your website”)
